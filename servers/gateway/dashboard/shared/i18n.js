/**
 * Translate a key to the given language.
 * Fallback: requested lang → en → raw key.
 * @param {string} key - Dot-separated translation key (e.g. "nav.messages")
 * @param {string} [lang="en"] - Language code
 * @returns {string}
 */
export function t(key, lang = "en") {
  const entry = translations[key];
  if (!entry) return key;
  return entry[lang] || entry.en || key;
}

/**
 * JS-safe version of t() for embedding in template literal JS strings.
 * Escapes single quotes, backslashes, backticks, and ${} sequences.
 * Use this instead of t() when the result will be inside a JS string literal.
 * @param {string} key
 * @param {string} [lang="en"]
 * @returns {string}
 */
export function tJs(key, lang = "en") {
  const val = t(key, lang);
  return val
    .replace(/\\/g, "\\\\")
    .replace(/'/g, "\\'")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
}
